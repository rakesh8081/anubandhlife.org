<script>
    const user = JSON.parse(localStorage.getItem('mindLabUser'));
    const scores = JSON.parse(localStorage.getItem('mindLabScores_interest'));

    if (!user || !scores) {
        window.location.href = 'index.php'; 
    }

    const dateObj = new Date();
    const dateString = dateObj.toLocaleDateString();
    
    // Adjusted the metadata layout to avoid line break issues in the PDF
    document.getElementById('reportMeta').innerHTML = `
        <strong>Participant:</strong> ${user.name} &nbsp;|&nbsp; <strong>Age:</strong> ${user.age} &nbsp;|&nbsp; <strong>Sex:</strong> ${user.sex} <br>
        <span style="display:inline-block; margin-top:5px;"><strong>Assessment Date:</strong> ${dateString}</span>
    `;

    const descriptions = {
        "P": { 
            name: "Practical (Doer)", 
            core: "You prefer working with tools, machines, or physical systems rather than extensive interpersonal interactions.",
            env: "Hands-on settings, outdoors, engineering workshops, or technical environments.",
            roles: "Mechanical Engineering, Construction Management, Skilled Trades, Systems Architecture, or Network Engineering."
        },
        "E": { 
            name: "Enterprising (Persuader)", 
            core: "You enjoy taking risks, leading projects, and influencing others to achieve organizational goals.",
            env: "Corporate management, law firms, entrepreneurship hubs, or competitive business environments.",
            roles: "Sales, Business Management, Entrepreneurship, Law, Real Estate, or Public Relations."
        },
        "S": { 
            name: "Social (Helper)", 
            core: "You are drawn to roles that involve teaching, healing, or developing others.",
            env: "Counseling centers, classrooms, healthcare facilities, or community organizations.",
            roles: "Teaching/Education, Counseling and Therapy, Human Resources, Nursing, Social Work, or the Non-Profit Sector."
        },
        "C": { 
            name: "Creative (Creator)", 
            core: "You enjoy developing your skills in art, music, or writing, and prefer to work with your mind, body, or feelings.",
            env: "Design studios, media outlets, the arts, or any workspace that values originality over strict, repetitive routines.",
            roles: "Graphic Design, Writing/Journalism, Architecture, Fine Arts, Marketing/Advertising, or Film and Video Production."
        },
        "I": { 
            name: "Investigative (Thinker)", 
            core: "You enjoy intellectual challenges, focusing on complex ideas, and using your analytical skills to uncover truths.",
            env: "Research facilities, academia, data analysis centers, or unstructured environments that allow for deep focus.",
            roles: "Scientific Research, Data Science, Psychology, Medical Professions, Forensics, or Software Development."
        },
        "O": { 
            name: "Organisational (Organizer)", 
            core: "You thrive when working with data, structured systems, and clear procedures.",
            env: "Financial institutions, administration, structured corporate offices, or logistical planning.",
            roles: "Accounting, Office Administration, Logistics and Supply Chain, Actuarial Science, Quality Control, or Compliance."
        }
    };

    let scoreArray = Object.keys(scores).map(key => ({
        id: key,
        name: descriptions[key].name,
        score: scores[key],
        core: descriptions[key].core,
        env: descriptions[key].env,
        roles: descriptions[key].roles
    }));

    scoreArray.sort((a, b) => {
        if (b.score !== a.score) return b.score - a.score;
        return a.name.localeCompare(b.name);
    });

    let groupedResults = [];
    let currentGroup = [];
    let currentScore = scoreArray[0].score;

    for (let i = 0; i < scoreArray.length; i++) {
        if (scoreArray[i].score < currentScore) {
            groupedResults.push(currentGroup);
            currentGroup = [];
            currentScore = scoreArray[i].score;
            if (groupedResults.length === 3) break; 
        }
        currentGroup.push(scoreArray[i]);
    }
    if (groupedResults.length < 3 && currentGroup.length > 0) {
        groupedResults.push(currentGroup);
    }

    const container = document.getElementById('results-container');
    let htmlContent = '';

    groupedResults.forEach((group, index) => {
        const rank = index + 1;
        
        if (group.length === 1) {
            const item = group[0];
            // ADDED pdf-protect class here
            htmlContent += `
                <div class="result-card pdf-protect">
                    <h3>#${rank}: ${item.name} (${item.score} pts)</h3>
                    <div class="trait-section"><strong>Core Trait:</strong> ${item.core}</div>
                    <div class="trait-section"><strong>Ideal Environments:</strong> ${item.env}</div>
                    <div class="trait-section"><strong>Suggested Industries & Roles:</strong> ${item.roles}</div>
                </div>
            `;
        } else {
            // ADDED pdf-protect class here
            htmlContent += `
                <div class="result-card pdf-protect">
                    <h3>#${rank}: Tied Interest Areas (${group[0].score} pts)</h3>
                    <p style="color: var(--text-muted); font-size: 0.9rem; margin-bottom: 10px;">You scored equally high in these distinct areas.</p>
            `;
            group.forEach(tiedItem => {
                htmlContent += `
                    <div class="tied-item">
                        <h4>${tiedItem.name}</h4>
                        <div class="trait-section"><strong>Core Trait:</strong> ${tiedItem.core}</div>
                        <div class="trait-section"><strong>Ideal Environments:</strong> ${tiedItem.env}</div>
                        <div class="trait-section"><strong>Suggested Roles:</strong> ${tiedItem.roles}</div>
                    </div>
                `;
            });
            htmlContent += `</div>`;
        }
    });

    container.innerHTML = htmlContent;

    function retakeTest() {
        localStorage.removeItem('mindLabScores_interest');
        window.location.href = 'test-interest.php';
    }
</script>

