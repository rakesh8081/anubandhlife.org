                        <div class="trait-section"><strong>Suggested Roles:</strong> ${tiedItem.roles}</div>
                    </div>
                `;
            });
            htmlContent += `</div>`;
        }
    });

    container.innerHTML = htmlContent;
});

// Mailer Function
function emailResults() {
    const participantId = localStorage.getItem('participant_id');
    const btn = document.getElementById('emailBtn');
    
    if (!participantId) {
        alert("Error: User session not found.");
        return;
    }

    // Visual feedback
    btn.innerText = "Sending...";
    btn.disabled = true;

    fetch('mailer.php', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ participant_id: participantId, type: 'interest' })
    })
    .then(res => res.json())
    .then(data => {
        if (data.success) {
            btn.innerText = "Sent Successfully!";
            btn.style.background = "#2e7d32"; 
            btn.style.color = "white";
            btn.style.borderColor = "#2e7d32";
        } else {
            alert("Delivery failed: " + data.message);
            btn.innerText = "Email Results";
            btn.disabled = false;
        }
    })
    .catch(err => {
        console.error(err);
        alert("Network error. Please try again.");
        btn.innerText = "Email Results";
        btn.disabled = false;
    });
}

